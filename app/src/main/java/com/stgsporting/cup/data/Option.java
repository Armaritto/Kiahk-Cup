package com.stgsporting.cup.data;

public class Option {

    private String optionName;
    private int stars;

    public Option() {}

    public String getOptionName() {
        return optionName;
    }

    public void setOptionName(String optionName) {
        this.optionName = optionName;
    }

    public int getStars() {
        return stars;
    }

    public void setStars(int stars) {
        this.stars = stars;
    }
}
